package homework.leetcode;

public class S2235 {
    // https://leetcode.com/problems/add-two-integers/description/

    public static void main(String[] args) {
        System.out.println(sum(-10, 4));
        // Output: 17
    }

    // num1 + num2
    public static int sum(int num1, int num2) {
        int answer = 0;

        answer = num1 + num2;

        return answer;
    }
}
